class Teacher {
    constructor(id, firstName, lastName, email, phone, department, hireDate, salary) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.phone = phone;
        this.department = department;
        this.hireDate = hireDate;
        this.salary = salary;
    }
}

module.exports = Teacher;